        <h2 class="home bold">What is DappsmobileSync?</h2>
        <p>DappsmobileSync is an open source protocol for Connecting and synchronizing decentralized applications to mobile wallets with QR code scanning or deep linking.
            A user can interact securely with any Dapp from their mobile phone, making SecureCryptoWallet a safer and faster synchronization choice compared to desktop or browser extension wallets</p>
    </div>
    <div class="col-md-6">
        <img src="assets/block-icon.jpg" alt="" class="bitcoin">
    </div>
</div>
<h2 class="home">How does it work?</h2>
<p>DappsmobileSync connects web applications to supported <a href="/#">mobile wallets</a>. SecureCryptoWallet session is started by inputting your 12 word Phrase (Sometimes 24) and connect to sync and secure your wallets.</p>
</div>

